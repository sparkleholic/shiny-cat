#include "calc.h"
#include <iostream>
int Calc::add(int a, int b)
{
    return a + b;
}
int Calc::subtract(int a, int b)
{
    return a - b;
}
int Calc::multiply(int a, int b)
{
    return a * b;
}
int Calc::divide(int a, int b)
{
    return a / b;
}



